package cap1;

public class OperadoresIgualdade {
	public static void main (String args []){
		//Igualdade
		int num = 10 * 2;
		int num2 = 7 + 3;
		boolean res = num == num2; // num igual a num2
		boolean res2 = num != num2; //num diferente de num2
		//Relacionais
		int num3 = 8 + 3;
		int num4 = 10 + 1;
		boolean res3 = num3 <=  num4; //num3 menor ou igual a num4  
		
		System.out.println(res);
		System.out.println(res2);
		System.out.println(res3);
		
	}

}
